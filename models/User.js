const mongoose = require('mongoose');
const userSchema = new mongoose.Schema({
	username:{
	  type:String,
	  require:true,
	},
	email:{
	  type:String,
	  require:true,
	  max:50,
	  unique:true
	},
	password:{
		type:String,
		require:true,
		min:6
	},
	DOB:{
	  type:String,
	  default:""
	},
	profilePicture:{
	  type:String,
	  default:""
	},
	coverPicture:{
		type:String,
		default:""
	},
	followers:{
		type:Array,
	},
	followings:{
		type:Array,
	},
	desc:{
	  type:String,
	  max:50
	},
	city:{
	  type:String,
	  max:50
	},
	from:{
	  type:String,
	  max:50
	},
	relationship:{
	  type:Number,
	  enum:[1,2,3]
	},
	sharedPost:{
	  type:Array
	},
	isAdmin:{
		type:Boolean,
		default:false
	}
},{timestamp:true})

const User = mongoose.model('User', userSchema);
module.exports = User;